<h4>Notas:</h4>
<p style="text-align: justify;">
En localidades donde existan sucursales del IPSFA , el afiliado podrá consignar los recaudos y este lo remitirá a la sede  principal .
Los recaudos podrán ser consignados directamente en la taquilla de reembolso que funcione en el módulo de información de la planta baja del edificio Sede en Caracas.<br>
El Afiliado debe presentar una factura para cada concepto.<br>
Las facturas deben estar identificadas con el Nombre del Militar o Familiar inmediato (Beneficiario)  a quien se le prestó el servicio.<br>
Las facturas o récipes médicos para complemento prescriben a los seis (6) meses  y un (1) año para los casos del exterior.<br>
Para los casos odontológicos se sugiere explicar detalladamente en la factura el tipo de tratamiento.<br>
El instituto NO RECONOCERÁ los gastos odontológicos cuando el tratamiento sea efectuado  en las localidades donde exista clínicas odontológicas del IPSFA, a excepción de alguna especialidad no realizadas en la clínica del instituto.<br>
Cuando la casa Comercial no acepte “Carta Aval “ el afiliado deberá cancelar el servicio, solicitar la indemnización a Seguros Horizontes C. A y luego gestionar ante el IPSFA el reembolso correspondiente, cumpliendo con los requisitos exigidos para los casos números 1 y 4.<br>
Consignar copia de la libreta de ahorro o cheque personalizado (militar o sobreviviente).<br>
Las facturas tendrán vigencia  de treinta (30) a partir de su emisión.<br>
No se reembolsan Medicinas.<br>
El instituto se reserva el derecho de realizar cualquier investigación social a los casos que de acuerdo a su situación se considere pertinente.<br>
